    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() ||
            jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty() ||
            jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty() ||
            jTextField7.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter all fields!");
        }
        else {
            String name = jTextField1.getText().trim();
            String shape = jTextField4.getText().trim();
            String size = jTextField3.getText().trim();
            String contents = jTextField2.getText().trim();
            String charac = jTextField7.getText().trim();
            String bulge = jTextField6.getText().trim();
            String gas = jTextField5.getText().trim();

            Galaxy galaxies = new Galaxy(name, shape, size, contents, charac,
                bulge, gas);

            galaxy.add(galaxies);

            saveGalaxyToFile();
            
            System.out.println("Data have been saved!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

