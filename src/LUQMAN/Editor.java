    public void populateArrayList(){
        try {
            FileInputStream file = new FileInputStream("Galaxy.txt");
            ObjectInputStream inputFile = new ObjectInputStream(file);
            
            boolean endOfFile = false;
            
            while (!endOfFile){
                try{
                    galaxy.add((Galaxy)inputFile.readObject());
                }
                catch (EOFException e){
                    endOfFile = true;
                }
                catch (Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            inputFile.close();
        }
        catch (IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public void saveGalaxyToFile(){
        try{
            FileOutputStream file = new FileOutputStream("Galaxy.txt");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            
            for (int i = 0; i < galaxy.size(); i++){
                outputFile.writeObject(galaxy.get(i));
            }
            outputFile.close();
            
            JOptionPane.showMessageDialog(null, "Successfully saved!");
            this.dispose();
        }
        catch (IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
