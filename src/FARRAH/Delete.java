    private void jButtonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveActionPerformed
 
        if (jTextNum.getText().isEmpty() || jTextOperator.getText().isEmpty() || jTextAmount.getText().isEmpty() || jTextPay.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null, "Please Enter All Field!");
        }
        else {
            int selectedIndex = OrderCodeDelete.getSelectedIndex();

            OrderList.remove(selectedIndex);

            saveToFile();
        }

    }//GEN-LAST:event_jButtonRemoveActionPerformed

