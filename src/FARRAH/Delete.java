    public Delete() {
        initComponents();
        OrderList = new ArrayList<>();
        populateArrayList();   
        this.setLocationRelativeTo(null);
        String[] orderCodeChoice =  new String[OrderList.size()];
        
        for(int i = 0 ; i < OrderList.size() ; i++)
        {
            orderCodeChoice[i] = OrderList.get(i).getCode();
        }
        OrderCodeDelete.setModel(new javax.swing.DefaultComboBoxModel<>(orderCodeChoice));
        
        OrderCodeDelete.setSelectedIndex(0);
        
    }

    public void populateArrayList(){
        try{
          FileInputStream file = new FileInputStream("order.txt");
          ObjectInputStream inputFile = new ObjectInputStream(file);
          
          boolean endOfFile = false;
          while(!endOfFile)
          {
             try
             {
               OrderList.add((OrderMobile)inputFile.readObject());  
             } 
             catch(EOFException e)
             {
                endOfFile = true;
             }
             catch (Exception f) 
             {
               JOptionPane.showMessageDialog(null, f.getMessage());  
             }
          }//while
        inputFile.close(); 
        }//try
        catch(IOException e){
          JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public void saveToFile()
    {
        try
        {
            FileOutputStream file = new FileOutputStream("order.txt");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            
            for(int i = 0; i < OrderList.size(); i++)
            {
                outputFile.writeObject(OrderList.get(i));
            }
            outputFile.close();
             JOptionPane.showMessageDialog(null, "Data Successfully Deleted.");
             this.dispose();
        }  
        catch(IOException e)
        {
           JOptionPane.showMessageDialog(null, e.getMessage());         
        }
    }

